from math import *

print(pi)   # Π的值
print(e)    # e的值
print(abs(-10)) # 绝对值函数(内置)
print(ceil(4.1)) # 向上取整函数
print(exp(1))  # e次幂函数
print(fabs(-10))    # 绝对值函数
print(floor(4.1))   # 向下取整函数
print(log(e))   # 返回e为底数的对数
print(log(100, 10)) # 返回10为底数100为真数的对数
print(log10(100))   # 返回10为底数的对数
print(max([1,2,3,4,5,6,7,8]))   # 返回序列的最大值(内置)
print(min([1,2,3,4,5,6,7,8]))   # 返回序列的最小值(内置)
print(modf(2.5))    # 以元组形式返回给定数的整数部分和小数部分
print(pow(2,5)) # 返回2的5次幂
print(round(6.5))   # 返回给定数的四舍五入值
print(round(5.4567, 3)) # 返回给定数四舍五入到n位数后的值
print(sqrt(4))  # 返回x的平方根(内置)
print(sin(pi/2))    # 正弦
print(cos(pi/2))    # 余弦
print(tan(pi/2))    # 正切
print(asin(1))   # 反正弦
print(acos(1))   # 反余弦
print(atan(1))   # 反正切
print(radians(57.3))  # 角度转化为弧度
